package com.auradevil.cachesuite.util;

import java.awt.*;

/**
 * @author tom
 */
public class Material3DS {
	private String name;
	private Color colour;

	public Material3DS() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Color getColour() {
		return colour;
	}

	public void setColour(Color colour) {
		this.colour = colour;
	}
}
